package com.singh.javain1video.conditionals;

public class Loops {
    public static void main(String[] args) {

        // WHILE LOOP
        int i = 0;
        while (i<=100){
            System.out.println("Hello " + i);
            i++;
        }

        // FOR LOOP
        for (int j=0; j<=100; j++){
            System.out.println("Heyy " + j);
        }

        // DO WHILE LOOP
        int k = 101;

        do {
            System.out.println(k);
            k++;
        }while (k<=100);

    }
}
